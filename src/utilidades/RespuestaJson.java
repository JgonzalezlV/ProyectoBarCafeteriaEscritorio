package utilidades;

/**
 *
 * @author negu2
 */
public class RespuestaJson {
    
    String code,value;

    public RespuestaJson(String code, String value) {
        this.code = code;
        this.value = value;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    @Override
    public String toString() {
        return "RespuestaJson{" + "code=" + code + ", values=" + value + '}';
    }   
}
