    /**
     * Metodo que comprueba si los campos estan vacios o no.
     * @param evt --> Es el MouseEvent que necesita el metodo.
     */
    private void panelLoginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_panelLoginMouseClicked
        // TODO add your handling code here:
        String usuario = txtUsuario.getText();
        char[] Arraypassword = txtPassword.getPassword();
        String password = String.valueOf(Arraypassword);
        // Se comprueba el usuario si no esta en blanco o vacio
        if (!usuario.isEmpty()) {
            // se pasa a la password y si esto no esta vacio
            if (!password.isEmpty()) {
                // llamamos al siguiente metodo
                login(usuario, password);
            } else {
                // Si la password esta en blanco saltara el siguiente JOptionPane
                JOptionPane.showMessageDialog(this, "Campo de la password no relleno.", "Password", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            // Si el usuario esta en blanco saltara el siguiente JOptionPane
            JOptionPane.showMessageDialog(this, "Campo del usuario no relleno.", "Usuario", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_panelLoginMouseClicked

